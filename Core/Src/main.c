int main(void)
{
  SCB->VTOR = FLASH_BASE;
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_SPI1_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_USB_DEVICE_Init();
  MX_TIM6_Init();

  mouse_init();
  mouse.read_settings();
  //mouse.write_settings(test_settings);
  wait_for_tick_start();
  uint32_t poll_timer = HAL_GetTick();
  while (1)
  {
	  //Enter loop once systick increments
	  if(HAL_GetTick() >= (poll_timer + 1) ){
		  mouse.update_led();
		  mouse.read_wheel();
		  mouse.read_buttons();
		  mouse.read_sensor();
		  if(hUsbDeviceFS.dev_state == USBD_STATE_CONFIGURED){

			  if(mouse_events.change_detected()){
				  USBD_CUSTOM_HID_SendReport(&hUsbDeviceFS, mouse.get_mouse_report(),MOUSE_REPORT_SIZE,MOUSE_EP_IN_ADDR);
			  }

			  if(mouse_events.key_change_detected()){
				  USBD_CUSTOM_HID_SendReport(&hUsbDeviceFS, mouse.get_key_report(),KEY_REPORT_SIZE,KEYBD_EP_IN_ADDR);
			  }

			  if((mouse_events.new_settings_flag_status()) == MOUSE_CHANGE){
				  mouse.write_settings(new_settings);
			  }

		  }
		  //update poll timer
		  poll_timer = HAL_GetTick();
	  }
  }

}

